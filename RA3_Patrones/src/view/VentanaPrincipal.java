    private void actualizarIndicadores() {

        lblCreado.setForeground(Color.GRAY);
        lblPagado.setForeground(Color.GRAY);
        lblEnviado.setForeground(Color.GRAY);
        lblEntregado.setForeground(Color.GRAY);

        if (pedido == null) {
            return;
        }

        switch (pedido.getEstado()) {

            case "CREADO":

                lblCreado.setText("💚 Creado");
                lblCreado.setForeground(new Color(0, 153, 0));
                break;

            case "PAGADO":

                lblCreado.setText("💚 Creado");
                lblPagado.setText("💚 Pagado");

                lblCreado.setForeground(new Color(0, 153, 0));
                lblPagado.setForeground(new Color(0, 153, 0));
                break;

            case "ENVIADO":

                lblCreado.setText("💚 Creado");
                lblPagado.setText("💚 Pagado");
                lblEnviado.setText("💚 Enviado");

                lblCreado.setForeground(new Color(0, 153, 0));
                lblPagado.setForeground(new Color(0, 153, 0));
                lblEnviado.setForeground(new Color(0, 153, 0));
                break;

            case "ENTREGADO":

                lblCreado.setText("💚 Creado");
                lblPagado.setText("💚 Pagado");
                lblEnviado.setText("💚 Enviado");
                lblEntregado.setText("💚 Entregado");

                lblCreado.setForeground(new Color(0, 153, 0));
                lblPagado.setForeground(new Color(0, 153, 0));
                lblEnviado.setForeground(new Color(0, 153, 0));
                lblEntregado.setForeground(new Color(0, 153, 0));
                break;
        }
    }

