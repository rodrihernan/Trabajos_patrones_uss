    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        // TODO add your handling code here:
        String nombreCliente = txtCliente.getText().trim();
        String nombreProducto = cbProducto.getSelectedItem().toString();
        if (nombreCliente.isEmpty() || nombreProducto.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Complete todos los campos.");

            return;
        }
        int cantidad = ((Number) spCantidad.getValue()).intValue();
        String metodoPago = cbMetodoPago.getSelectedItem().toString();
        Cliente cliente = new Cliente(nombreCliente);
        Producto producto;
        switch (nombreProducto) {
            case "Laptop Lenovo ThinkPad":
                producto = new Producto(nombreProducto, 3500);
                break;
            case "iPhone 15 Pro":
                producto = new Producto(nombreProducto, 4200);
                break;
            case "Smart TV Samsung 55\"":
                producto = new Producto(nombreProducto, 2500);
                break;
            case "Refrigeradora LG":
                producto = new Producto(nombreProducto, 2800);
                break;
            default:
                producto = new Producto(nombreProducto, 1900);
        }
        pedido = new Pedido(cliente, producto, cantidad, metodoPago);
        pedido.agregarObserver(historialObserver);
        pedido.agregarObserver(estadoObserver);
        CrearPedidoCommand comando = new CrearPedidoCommand(pedido);
        invoker.ejecutarComando(comando);
        actualizarIndicadores();
        lblEstado.setText(pedido.getEstado());

        txtHistorial.append("====================================\n");
        txtHistorial.append("Pedido creado\n");
        txtHistorial.append("Cliente : " + cliente.getNombre() + "\n");
        txtHistorial.append("Producto: " + producto.getNombre() + "\n");
        txtHistorial.append("Cantidad : " + cantidad + "\n");
        txtHistorial.append("Precio Unitario : S/. " + producto.getPrecio() + "\n");
        txtHistorial.append("TOTAL : S/. " + pedido.calcularTotal() + "\n");
        txtHistorial.append("Pago    : " + metodoPago + "\n");
        txtHistorial.append("Estado  : " + pedido.getEstado() + "\n\n");
    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnPagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPagarActionPerformed
        // TODO add your handling code here:
        if (pedido == null) {

            JOptionPane.showMessageDialog(this,
                    "Primero debe crear un pedido.");
            return;
        }

        MetodoPago metodo;
        String opcion = cbMetodoPago.getSelectedItem().toString();
        switch (opcion) {
            case "Tarjeta":
                metodo = new PagoTarjeta();
                break;
            case "Yape":
                metodo = new PagoYape();
                break;
            default:
                metodo = new PagoEfectivo();
                break;
        }

        contextoPago.setMetodoPago(metodo);
        double monto = pedido.calcularTotal();
        contextoPago.procesarPago(monto);
        PagarPedidoCommand comando = new PagarPedidoCommand(pedido);
        invoker.ejecutarComando(comando);
        actualizarIndicadores();
    }//GEN-LAST:event_btnPagarActionPerformed

